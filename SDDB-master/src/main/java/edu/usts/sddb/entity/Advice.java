package edu.usts.sddb.entity;

public class Advice {
	private Integer ad_id;
	private String ad_category;
	private String ad_level;
	private String ad_suggestion;

	public Integer getAd_id() {
		return ad_id;
	}

	public void setAd_id(Integer ad_id) {
		this.ad_id = ad_id;
	}

	public String getAd_category() {
		return ad_category;
	}

	public void setAd_category(String ad_category) {
		this.ad_category = ad_category;
	}

	public String getAd_level() {
		return ad_level;
	}

	public void setAd_level(String ad_level) {
		this.ad_level = ad_level;
	}

	public String getAd_suggestion() {
		return ad_suggestion;
	}

	public void setAd_suggestion(String ad_suggestion) {
		this.ad_suggestion = ad_suggestion;
	}

}
